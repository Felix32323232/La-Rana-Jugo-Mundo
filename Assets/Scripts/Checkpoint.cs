using UnityEngine;

public class Checkpoint : MonoBehaviour
{
    private void OnTriggerEnter2D(Collider2D other)
    {
        if (other.CompareTag("Player"))
        {
            Timer player = other.GetComponent<Timer>();
            if (player != null)
            {
                player.SetCheckpoint(transform.position);
              
            }
        }
    }
}